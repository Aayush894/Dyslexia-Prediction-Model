import { useState } from "react";
import { Link, useNavigate } from "react-router-dom";
import Footer from "../../components/Footer/Footer";
import toast from "react-hot-toast";
import Swal from 'sweetalert2'

function Signup() {
  const [credentials, setCredentials] = useState({
    username: "",
    email: "",
    password: "",
    age: "",
    phoneno: "",
  });

  const navigate = useNavigate();

  const handleSubmit = async (e) => {
    e.preventDefault();
    try {
      if (Validate({ ...credentials })) return; 

      const response = await fetch("/api/signup", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          username: credentials.username,
          email: credentials.email,
          password: credentials.password,
          age: credentials.age,
          phoneno: credentials.phoneno,
        }),
      });

      const json = await response.json();
      
      if (!json.success) {
        console.log(json.message);
        throw new Error(json.message);
      } else {
        toast.success("Signup Successful!");
        navigate("/login");
        
      }
    } catch (error) {
      console.error("Error while fetching or processing result:", error);
      toast.error("Server Error", error.message);
    }
  };

  const onChange = (e) => {
    setCredentials({ ...credentials, [e.target.name]: e.target.value });
  };

  return (
    <div>
      <div className="m-auto">
        <section className="bg-gray-50 dark:bg-gray-900">
          <div className="flex flex-col items-center justify-center px-6 py-8 mx-auto md:h-screen lg:py-0 mb-20 mt-10">
            <Link
              to="/"
              className="flex items-center space-x-2 text-4xl font-medium text-indigo-500 dark:text-gray-100 font-sans mt-10"
            >
              DysLexiLens
            </Link>
            <div className="w-full bg-white rounded-lg shadow dark:border md:mt-0 sm:max-w-md xl:p-0 dark:bg-gray-800 dark:border-gray-700">
              <div className="p-6 space-y-4 md:space-y-6 sm:p-8">
                <h1 className="text-xl font-semibold leading-tight tracking-tight text-gray-900 md:text-2xl dark:text-white">
                  Create Account
                </h1>
                <form
                  className="space-y-4 md:space-y-6"
                  onSubmit={handleSubmit}
                >
                  <div>
                    <label
                      htmlFor="username"
                      className="block mb-2 text-sm font-medium text-gray-900 dark:text-white"
                    >
                      UserName
                    </label>
                    <input
                      type="text"
                      name="username"
                      id="username"
                      className="bg-gray-50 border border-gray-300 text-gray-900 sm:text-sm rounded-lg focus:ring-primary-600 focus:border-primary-600 block w-full p-2.5 dark:bg-gray-700 dark:border-gray-600 dark:placeholder-gray-400 dark:text-white dark:focus:ring-blue-500 dark:focus:border-blue-500"
                      placeholder="name123"
                      value={credentials.username}
                      onChange={onChange}
                    />
                  </div>
                  <div>
                    <label
                      htmlFor="email"
                      className="block mb-2 text-sm font-medium text-gray-900 dark:text-white"
                    >
                      Your email
                    </label>
                    <input
                      type="email"
                      name="email"
                      id="email"
                      className="bg-gray-50 border border-gray-300 text-gray-900 sm:text-sm rounded-lg focus:ring-primary-600 focus:border-primary-600 block w-full p-2.5 dark:bg-gray-700 dark:border-gray-600 dark:placeholder-gray-400 dark:text-white dark:focus:ring-blue-500 dark:focus:border-blue-500"
                      placeholder="name@company.com"
                      value={credentials.email}
                      onChange={onChange}
                    />
                  </div>
                  <div>
                    <label
                      htmlFor="password"
                      className="block mb-2 text-sm font-medium text-gray-900 dark:text-white"
                    >
                      Password
                    </label>
                    <input
                      type="password"
                      name="password"
                      id="password"
                      placeholder="password"
                      className="bg-gray-50 border border-gray-300 text-gray-900 sm:text-sm rounded-lg focus:ring-primary-600 focus:border-primary-600 block w-full p-2.5 dark:bg-gray-700 dark:border-gray-600 dark:placeholder-gray-400 dark:text-white dark:focus:ring-blue-500 dark:focus:border-blue-500"
                      value={credentials.password}
                      onChange={onChange}
                    />
                  </div>
                  <div>
                    <label
                      htmlFor="phoneno"
                      className="block mb-2 text-sm font-medium text-gray-900 dark:text-white"
                    >
                      Phone No.
                    </label>
                    <input
                      type="number"
                      name="phoneno"
                      id="phoneno"
                      placeholder="1234567XXX"
                      className="bg-gray-50 border border-gray-300 text-gray-900 sm:text-sm rounded-lg focus:ring-primary-600 focus:border-primary-600 block w-full p-2.5 dark:bg-gray-700 dark:border-gray-600 dark:placeholder-gray-400 dark:text-white dark:focus:ring-blue-500 dark:focus:border-blue-500"
                      value={credentials.phoneno}
                      onChange={onChange}
                    />
                  </div>
                  <div>
                    <label
                      htmlFor="age"
                      className="block mb-2 text-sm font-medium text-gray-900 dark:text-white"
                    >
                      Age
                    </label>
                    <input
                      type="number"
                      name="age"
                      id="age"
                      placeholder="age"
                      className="bg-gray-50 border border-gray-300 text-gray-900 sm:text-sm rounded-lg focus:ring-primary-600 focus:border-primary-600 block w-full p-2.5 dark:bg-gray-700 dark:border-gray-600 dark:placeholder-gray-400 dark:text-white dark:focus:ring-blue-500 dark:focus:border-blue-500"
                      value={credentials.age}
                      onChange={onChange}
                    />
                  </div>

                  <div className="flex flex-col items-center justify-center">
                    <button
                      type="submit"
                      className="btn btn-primary btn-lg bg-primary mt-0 mb-4 px-8 py-2"
                    >
                      SignUp
                    </button>
                    <p className="text-sm font-light text-gray-500 dark:text-gray-400">
                      Already have an account?{" "}
                      <Link
                        to="/login"
                        className="font-medium text-primary-600 hover:underline dark:text-primary-500"
                      >
                        Sign in
                      </Link>
                    </p>
                  </div>
                </form>
              </div>
            </div>
          </div>
        </section>
      </div>
      <div>
        <Footer />
      </div>
    </div>
  );
}

export default Signup;

const Validate = (credentials) => {
  if (!credentials.username) {
    Swal.fire({
      title: 'Error!',
      text: 'Username is required',
      icon: 'error',
      confirmButtonText: 'Cool',
      timer: 2500
    })
    return true ;
  }
  if (!credentials.email) {
    Swal.fire({
      title: 'Error!',
      text: 'Email is required',
      icon: 'error',
      confirmButtonText: 'Cool',
      timer: 2500
    })
    return true ;
  }
  if (!credentials.password) {
    Swal.fire({
      title: 'Error!',
      text: 'Password is required',
      icon: 'error',
      confirmButtonText: 'Cool',
      timer: 2500
    })
    return true ;
  }
  if (!credentials.phoneno) {
    Swal.fire({
      title: 'Error!',
      text: 'Phone Number is required',
      icon: 'error',
      confirmButtonText: 'Cool',
      timer: 2500
    })
    return true ;
  }
  if (!credentials.age) {
    Swal.fire({
      title: 'Error!',
      text: 'Age is required',
      icon: 'error',
      confirmButtonText: 'Cool',
      timer: 2500
    })
    return true ;
  }
  

  if (credentials.username.length) {
    const usernameRegex = /^[a-zA-Z]+\d*$/;
    if (!usernameRegex.test(credentials.username)) {
      Swal.fire({
        title: 'Error!',
        text: 'Username must consist of lowercase  or uppercase letters followed by digits',
        icon: 'error',
        confirmButtonText: 'Cool',
        timer: 2500
      })
      return true ;
    }
  }

  if (credentials.password.length < 4) {
    Swal.fire({
      title: 'Error!',
      text: 'Password must be atleast 4 characters long',
      icon: 'error',
      confirmButtonText: 'Cool',
      timer: 2500
    })
    return true ;
  }

  return false ;

}